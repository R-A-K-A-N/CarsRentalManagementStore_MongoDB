    private void jTable_CarImagesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_CarImagesMouseClicked
        //display the selected image
        // get image id from the jtable
        int index = jTable_CarImages.getSelectedRow();
        //System.out.println("index ->" + index);
        int image_id = Integer.valueOf(jTable_CarImages.getValueAt(index, 0).toString());
        //System.out.println("image_id -> " + image_id);
        
        byte[] img = null;
        for (CarImage_DTO cimg: carsimage_list)
        {
            if(cimg.getImg_id() == image_id)
            {
                img = cimg.getCar_img();
            }
        }
        displayByteImage(jTable_CarImages.getWidth(), jTable_CarImages.getHeight(), img, jLabel_CarImage);
    }//GEN-LAST:event_jTable_CarImagesMouseClicked

