    public String selectImage()
    {
        JFileChooser fileChooser = new JFileChooser(); 
        fileChooser.setDialogTitle("Select Pricture");
        
        fileChooser.setCurrentDirectory (new File("D:\\Nosql\\CarsRentalManagementStore\\CarsRentalManagementStore\\src\\images\\car"));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("image", ".png",".jpg"); 
        fileChooser.addChoosableFileFilter(filter);
        
        int state = fileChooser.showSaveDialog (null);
        String path = ""; 
        if (state == JFileChooser.APPROVE_OPTION)
        {
            path = fileChooser.getSelectedFile().getAbsolutePath();
        }
        return path;
    }
    
    public void populateJTableWithCars() {
        //jtable columns
        String[] columnsName = {"ID", "Model", "Class"};
        
        // jtable rows
        Object[][] rows = new Object[cars_list.size()][columnsName.length];
        
        for(int i=0; i < cars_list.size(); i++)
        {
            rows[i][0] = cars_list.get(i).getCar_id();
            rows[i][1] = cars_list.get(i).getModel();
            rows[i][2] = cars_list.get(i).getClass_();
        }
        DefaultTableModel model = new DefaultTableModel(rows,columnsName);
        jTable_Cars.setModel(model);
    }
    
    public void populateJTableWithCarImages(int car_id) {
        carsimage_list = carsimageBus.carImagesList(car_id);
        //jtable columns
        String[] columnsName = {"Image ID"};
        
        // jtable rows
        Object[][] rows = new Object[carsimage_list.size()][columnsName.length];
        
        for(int i=0; i < carsimage_list.size(); i++)
        {
            rows[i][0] = carsimage_list.get(i).getImg_id();
        }
        DefaultTableModel model = new DefaultTableModel(rows,columnsName);
        jTable_CarImages.setModel(model);
    }
