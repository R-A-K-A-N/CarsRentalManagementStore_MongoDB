    public boolean verify() {
        String model = jTextField_Model.getText();
        String _class_ = jTextField_Class.getText();
        String brandIdText = jLabel_brand_id.getText();
        int id = (int) jSpinner_id.getValue();
        
        if (id == 0) {
           JOptionPane.showMessageDialog(null, "ID cannot be 0", "Invalid Info", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (brandIdText.equals("#") || !isNumeric(brandIdText)) {
            JOptionPane.showMessageDialog(null, "Select a valid brand", "Invalid Info", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (!model.trim().equals("") && !_class_.trim().equals("")) {
            if (!jRadioButton_Automatic.isSelected() && !jRadioButton_Manual.isSelected()) {
                JOptionPane.showMessageDialog(null, "Select a gearbox type (Automatic or Manual)", "Invalid Info", JOptionPane.WARNING_MESSAGE);
                return false;
            }
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Enter a Valid Model and Class", "Invalid Info", 2);
            return false;
        }
    }
