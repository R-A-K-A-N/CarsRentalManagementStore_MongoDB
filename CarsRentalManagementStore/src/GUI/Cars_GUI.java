    private void btn_undo_carActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_undo_carActionPerformed
        try {
            Cars_DTO restoredCar = carsBus.undoDeleteCar();

            if (restoredCar != null) {
                carsBus.restoreCar(restoredCar);
                JOptionPane.showMessageDialog(this, "Car restored successfully!", "Undo Car", JOptionPane.INFORMATION_MESSAGE);

            } else {
                JOptionPane.showMessageDialog(this, "No car to restore.", "Undo", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error in undo operation: " + ex.getMessage(), "Undo Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_undo_carActionPerformed

